    private static void ComposeContent(IContainer container, AnalysisResult result)
    {
        container.PaddingTop(16).Column(col =>
        {
            col.Spacing(22);

            col.Item().Element(c => ComposeCoverBlock(c, result));

            if (!string.IsNullOrWhiteSpace(result.ExecutiveSummary))
                col.Item().Element(c => ComposeSection(c, "Executive Summary", result.ExecutiveSummary));

            if (!string.IsNullOrWhiteSpace(result.TechnicalOverview))
                col.Item().Element(c => ComposeSection(c, "Technical Overview", result.TechnicalOverview));

            if (!string.IsNullOrWhiteSpace(result.ArchitectureDescription))
                col.Item().Element(c => ComposeSection(c, "Architecture Overview", result.ArchitectureDescription));

            if (result.Features.Count > 0)
                col.Item().Element(c => ComposeFeaturesSection(c, result.Features));

            if (!string.IsNullOrWhiteSpace(result.UserGuide))
                col.Item().Element(c => ComposeSection(c, "User Guide", result.UserGuide));

            if (!string.IsNullOrWhiteSpace(result.SetupInstructions))
                col.Item().Element(c => ComposeSection(c, "Setup Instructions", result.SetupInstructions));

            if (!string.IsNullOrWhiteSpace(result.ConfigurationGuide))
                col.Item().Element(c => ComposeSection(c, "Configuration Guide", result.ConfigurationGuide));

            if (result.ApiEndpoints.Count > 0)
                col.Item().Element(c => ComposeApiSection(c, result.ApiEndpoints));

            if (result.Dependencies.Count > 0)
                col.Item().Element(c => ComposeListSection(c, "Dependencies", result.Dependencies));

            if (result.Recommendations.Count > 0)
                col.Item().Element(c => ComposeListSection(c, "Recommendations", result.Recommendations, AccentColor));

            if (result.KnownIssues.Count > 0)
                col.Item().Element(c => ComposeListSection(c, "Known Issues", result.KnownIssues, WarnColor));
        });
    }

