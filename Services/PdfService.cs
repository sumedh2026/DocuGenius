    // ─── Public entry ─────────────────────────────────────────────────────────────

    public Task<string> GeneratePdfAsync(AnalysisResult result, string outputFileName)
    {
        Directory.CreateDirectory(Path.GetFullPath(_settings.PdfDirectory));

        var sanitized = string.Concat(outputFileName.Split(Path.GetInvalidFileNameChars()));
        if (!sanitized.EndsWith(".pdf", StringComparison.OrdinalIgnoreCase))
            sanitized += ".pdf";

        var outputPath = Path.Combine(Path.GetFullPath(_settings.PdfDirectory), sanitized);
        _logger.LogInformation("Generating PDF at {Path}", outputPath);

        Document.Create(container =>
        {
            container.Page(page =>
            {
                page.Size(PageSizes.A4);
                page.Margin(40);
                page.PageColor(Colors.White);
                page.DefaultTextStyle(x => x.FontFamily("Arial").FontSize(10).FontColor(DarkGray));

                page.Header().Element(ComposeHeader);
                page.Content().Element(c => ComposeContent(c, result));
                page.Footer().Element(ComposeFooter);
            });
        }).GeneratePdf(outputPath);

        _logger.LogInformation("PDF generated: {Path}", outputPath);
        return Task.FromResult(outputPath);
    }

