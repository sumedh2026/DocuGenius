    /// <summary>
    /// Parses markdown content into visual blocks and renders each one using QuestPDF.
    /// Handles: headings (#/##/###), bullet lists, numbered lists, code blocks,
    /// horizontal rules, blank-line-separated paragraphs, and inline bold/italic/code.
    /// </summary>
    private static void RenderMarkdown(IContainer container, string raw)
    {
        // If the raw text looks like a JSON dump (fallback path), render it as a code block
        var trimmed = raw.Trim();
        if ((trimmed.StartsWith("{") && trimmed.EndsWith("}")) ||
            (trimmed.StartsWith("[") && trimmed.EndsWith("]")))
        {
            container.Background(CodeBg).Padding(8)
                .Text(trimmed).FontFamily("Courier New").FontSize(8).FontColor(CodeFg);
            return;
        }

        var blocks = ParseMarkdownBlocks(NormalizeInlineNumberedLists(raw));

        container.Column(col =>
        {
            col.Spacing(5);

            foreach (var block in blocks)
            {
                switch (block.Type)
                {
                    case MdBlockType.H1:
                        col.Item().PaddingTop(8)
                            .Text(block.Text).FontSize(14).Bold().FontColor(PrimaryColor);
                        col.Item().Background(AccentColor).Height(1);
                        break;

                    case MdBlockType.H2:
                        col.Item().PaddingTop(6)
                            .Text(block.Text).FontSize(12).Bold().FontColor(PrimaryColor);
                        break;

                    case MdBlockType.H3:
                        col.Item().PaddingTop(4)
                            .Text(block.Text).FontSize(11).Bold().FontColor(AccentColor);
                        break;

                    case MdBlockType.H4:
                        col.Item().PaddingTop(2)
                            .Text(block.Text).FontSize(10).Bold();
                        break;

                    case MdBlockType.HRule:
                        col.Item().PaddingVertical(4).Background(Colors.Grey.Lighten2).Height(1);
                        break;

                    case MdBlockType.CodeBlock:
                        col.Item().Background(CodeBg).Padding(8)
                            .Text(block.Text)
                            .FontFamily("Courier New").FontSize(8.5f).FontColor(CodeFg).LineHeight(1.4f);
                        break;

                    case MdBlockType.Bullet:
                        col.Item().PaddingBottom(3).Row(row =>
                        {
                            if (block.Indent > 0)
                                row.ConstantItem(block.Indent * 14);

                            row.ConstantItem(14)
                                .AlignTop()
                                .Text("•").FontSize(11).FontColor(PrimaryColor);

                            row.RelativeItem().PaddingLeft(4)
                                .Element(c => RenderInlineText(c, block.Text, 10));
                        });
                        break;

                    case MdBlockType.Numbered:
                        col.Item().PaddingBottom(4).Row(row =>
                        {
                            if (block.Indent > 0)
                                row.ConstantItem(block.Indent * 14);

                            row.ConstantItem(26)
                                .AlignTop()
                                .Text($"{block.Number}.")
                                .FontSize(10).Bold().FontColor(PrimaryColor);

                            row.RelativeItem().PaddingLeft(4)
                                .Element(c => RenderInlineText(c, block.Text, 10));
                        });
                        break;

                    case MdBlockType.BlockQuote:
                        col.Item().BorderLeft(3).BorderColor(AccentColor)
                            .PaddingLeft(10).PaddingVertical(4)
                            .Element(c => RenderInlineText(c, block.Text, 10));
                        break;

                    case MdBlockType.Paragraph:
                    default:
                        col.Item().Element(c => RenderInlineText(c, block.Text, 10));
                        break;
                }
            }
        });
    }

    // ─── Inline text renderer (bold / italic / inline-code) ───────────────────────

    /// <summary>
    /// Renders a single line of text with inline markdown:
    /// **bold**, *italic*, __bold__, _italic_, `code`
    /// </summary>
    private static void RenderInlineText(IContainer container, string text, float fontSize)
    {
        var spans = ParseInlineSpans(text);

        if (spans.All(s => !s.Bold && !s.Italic && !s.Code))
        {
            // Fast path — no inline formatting
            container.Text(text).FontSize(fontSize).LineHeight(1.5f);
            return;
        }

        container.Text(t =>
        {
            t.DefaultTextStyle(s => s.FontSize(fontSize).LineHeight(1.5f));

            foreach (var span in spans)
            {
                var s = t.Span(span.Text);

                if (span.Code)
                {
                    s.FontFamily("Courier New")
                     .FontSize(fontSize - 0.5f)
                     .BackgroundColor(CodeBg)
                     .FontColor(CodeFg);
                }
                else
                {
                    if (span.Bold)   s.Bold();
                    if (span.Italic) s.Italic();
                }
            }
        });
    }

    // =========================================================================
    // Inline numbered-list normaliser
    // =========================================================================

    // A list marker is: digit(s) followed by ) or . then whitespace,
    // AND must be at start-of-string or preceded by whitespace.
    // We do NOT use a general "any number" match to avoid false-positives
    // from HTTP codes, version numbers, JSON values, etc.
    private static readonly Regex ListMarkerRx =
        new(@"\d+[).]\s+", RegexOptions.Compiled);

    // Safety gates: only process a line if it contains BOTH item 1 and item 2
    // as explicit list markers (e.g. "1) " or "1. " AND "2) " or "2. ").
    private static readonly Regex ItemOneRx =
        new(@"(^|\s)1[).]\s", RegexOptions.Compiled);
    private static readonly Regex ItemTwoRx =
        new(@"(^|\s)2[).]\s", RegexOptions.Compiled);

    /// <summary>
    /// Detects lines where numbered items are written inline
    /// (e.g. "Intro: 1) Do this. 2) Do that.") and rewrites them so each
    /// item is on its own line, allowing the block parser to render them
    /// as a proper numbered list.
    ///
    /// Safety: only triggers when the line explicitly contains both
    /// item 1 and item 2 markers to avoid false-positives on HTTP codes,
    /// version numbers, JSON values, etc.
    /// </summary>
    private static string NormalizeInlineNumberedLists(string raw)
    {
        var sb = new StringBuilder();

        foreach (var rawLine in raw.Replace("\r\n", "\n").Split('\n'))
        {
            var line = rawLine.TrimEnd();

            // Must have both "1) " / "1. " AND "2) " / "2. " to be treated as a list
            if (!ItemOneRx.IsMatch(line) || !ItemTwoRx.IsMatch(line))
            {
                sb.AppendLine(line);
                continue;
            }

            // Collect only markers that are at start-of-string or preceded by whitespace
            var markerMatches = ListMarkerRx.Matches(line)
                .Cast<Match>()
                .Where(m => m.Index == 0 || char.IsWhiteSpace(line[m.Index - 1]))
                .ToList();

            if (markerMatches.Count < 2)
            {
                sb.AppendLine(line);
                continue;
            }

            // Intro: everything before the first marker
            var intro = line[..markerMatches[0].Index].TrimEnd().TrimEnd(':').Trim();
            if (!string.IsNullOrWhiteSpace(intro))
                sb.AppendLine(intro + ":");

            // Each item: from end of its marker to start of the next marker
            for (int i = 0; i < markerMatches.Count; i++)
            {
                var m         = markerMatches[i];
                var numStr    = Regex.Match(m.Value, @"^\d+").Value;
                int textStart = m.Index + m.Length;
                int textEnd   = i + 1 < markerMatches.Count
                    ? markerMatches[i + 1].Index
                    : line.Length;

                // Strip trailing whitespace that belongs to the next marker's leading space
                while (textEnd > textStart && char.IsWhiteSpace(line[textEnd - 1]))
                    textEnd--;

                var text = line[textStart..textEnd].Trim();
                if (!string.IsNullOrWhiteSpace(text))
                    sb.AppendLine($"{numStr}. {text}");
            }
        }

        return sb.ToString();
    }

    // =========================================================================
    // Block parser
    // =========================================================================

    private enum MdBlockType
    { H1, H2, H3, H4, Paragraph, Bullet, Numbered, CodeBlock, HRule, BlockQuote }

    private record MdBlock(MdBlockType Type, string Text, int Number = 0, int Indent = 0);

    private static readonly Regex NumberedListRx =
        new(@"^(\s*)(\d+)[).]\s+(.+)$", RegexOptions.Compiled);

    private static readonly Regex BulletListRx =
        new(@"^(\s*)[-*+]\s+(.+)$", RegexOptions.Compiled);

    private static List<MdBlock> ParseMarkdownBlocks(string raw)
    {
        var blocks  = new List<MdBlock>();
        var lines   = raw.Replace("\r\n", "\n").Split('\n');
        var paraBuffer = new StringBuilder();
        bool inCode = false;
        var  codeBuf = new StringBuilder();

        void FlushParagraph()
        {
            var para = paraBuffer.ToString().Trim();
            if (para.Length > 0)
                blocks.Add(new MdBlock(MdBlockType.Paragraph, para));
            paraBuffer.Clear();
        }

        foreach (var rawLine in lines)
        {
            var line = rawLine.TrimEnd();

            // ── Code fence ──────────────────────────────────────────────────
            if (line.TrimStart().StartsWith("```"))
            {
                if (!inCode)
                {
                    FlushParagraph();
                    inCode = true;
                    codeBuf.Clear();
                }
                else
                {
                    blocks.Add(new MdBlock(MdBlockType.CodeBlock, codeBuf.ToString().TrimEnd()));
                    inCode = false;
                }
                continue;
            }

            if (inCode)
            {
                codeBuf.AppendLine(line);
                continue;
            }

            // ── Horizontal rule ─────────────────────────────────────────────
            if (Regex.IsMatch(line, @"^(\s*[-*_]){3,}\s*$"))
            {
                FlushParagraph();
                blocks.Add(new MdBlock(MdBlockType.HRule, string.Empty));
                continue;
            }

            // ── ATX Headings ────────────────────────────────────────────────
            var headingMatch = Regex.Match(line, @"^(#{1,4})\s+(.+)");
            if (headingMatch.Success)
            {
                FlushParagraph();
                var level = headingMatch.Groups[1].Value.Length;
                var text  = headingMatch.Groups[2].Value.Trim();
                var type  = level switch { 1 => MdBlockType.H1, 2 => MdBlockType.H2,
                                           3 => MdBlockType.H3, _ => MdBlockType.H4 };
                blocks.Add(new MdBlock(type, text));
                continue;
            }

            // ── Block quote ─────────────────────────────────────────────────
            if (line.TrimStart().StartsWith("> "))
            {
                FlushParagraph();
                blocks.Add(new MdBlock(MdBlockType.BlockQuote,
                    line.TrimStart().Substring(2).Trim()));
                continue;
            }

            // ── Numbered list ────────────────────────────────────────────────
            var numMatch = NumberedListRx.Match(line);
            if (numMatch.Success)
            {
                FlushParagraph();
                int indent = numMatch.Groups[1].Value.Length / 2;
                int num    = int.Parse(numMatch.Groups[2].Value);
                string txt = numMatch.Groups[3].Value;
                blocks.Add(new MdBlock(MdBlockType.Numbered, txt, num, indent));
                continue;
            }

            // ── Bullet list ──────────────────────────────────────────────────
            var bulletMatch = BulletListRx.Match(line);
            if (bulletMatch.Success)
            {
                FlushParagraph();
                int indent = bulletMatch.Groups[1].Value.Length / 2;
                string txt = bulletMatch.Groups[2].Value;
                blocks.Add(new MdBlock(MdBlockType.Bullet, txt, 0, indent));
                continue;
            }

            // ── Blank line = paragraph break ─────────────────────────────────
            if (string.IsNullOrWhiteSpace(line))
            {
                FlushParagraph();
                continue;
            }

            // ── Regular text — accumulate into paragraph ─────────────────────
            if (paraBuffer.Length > 0)
                paraBuffer.Append(' ');
            paraBuffer.Append(line.Trim());
        }

        // Flush any open blocks
        if (inCode && codeBuf.Length > 0)
            blocks.Add(new MdBlock(MdBlockType.CodeBlock, codeBuf.ToString().TrimEnd()));

        FlushParagraph();

        return blocks;
    }

    // =========================================================================
    // Inline span parser
    // =========================================================================

    private record InlineSpan(string Text, bool Bold = false, bool Italic = false, bool Code = false);

    /// <summary>
    /// Splits a string into spans honouring **bold**, *italic*, `code` markdown.
    /// Handles **bold** before *italic* to avoid partial matches.
    /// </summary>
    private static List<InlineSpan> ParseInlineSpans(string text)
    {
        var spans  = new List<InlineSpan>();
        var remain = text;

        // Pattern: `code` | **bold** | __bold__ | *italic* | _italic_
        var rx = new Regex(@"(`[^`]+`|\*\*[^*]+\*\*|__[^_]+__|(?<!\*)\*(?!\*)[^*]+(?<!\*)\*(?!\*)|(?<!_)_(?!_)[^_]+(?<!_)_(?!_))");

        int pos = 0;
        foreach (Match m in rx.Matches(text))
        {
            // Text before match
            if (m.Index > pos)
                spans.Add(new InlineSpan(text[pos..m.Index]));

            var raw = m.Value;

            if (raw.StartsWith("`") && raw.EndsWith("`"))
                spans.Add(new InlineSpan(raw[1..^1], Code: true));
            else if (raw.StartsWith("**") || raw.StartsWith("__"))
                spans.Add(new InlineSpan(raw[2..^2], Bold: true));
            else
                spans.Add(new InlineSpan(raw[1..^1], Italic: true));

            pos = m.Index + m.Length;
        }

        if (pos < text.Length)
            spans.Add(new InlineSpan(text[pos..]));

        return spans.Where(s => s.Text.Length > 0).ToList();
    }

    // ─── Terminal code block (dark bg, white text, pretty JSON) ──────────────────

    /// <summary>
    /// Renders content in a dark terminal-style box.
    /// If the text is valid JSON it is pretty-printed line by line.
    /// </summary>
    private static void RenderTerminalBlock(IContainer container, string content)
    {
        var formatted = PrettyPrintJson(content.Trim());

        container
            .Background(TerminalBg)
            .Border(1).BorderColor("#3C3C3C")
            .Padding(10)
            .Column(col =>
            {
                col.Spacing(0);

                // Render each line separately so line breaks are guaranteed
                foreach (var line in formatted.Split('\n'))
                {
                    col.Item()
                        .Text(line.TrimEnd())
                        .FontFamily("Courier New")
                        .FontSize(8.5f)
                        .FontColor(TerminalFg)
                        .LineHeight(1.5f);
                }
            });
    }

    /// <summary>
    /// Attempts to parse and re-serialize the string as indented JSON.
    /// Applies light cleanup for common AI quirks (single quotes, trailing commas,
    /// unquoted keys) before parsing. Falls back to manual indentation if still
    /// not valid JSON, so the output is always human-readable.
    /// </summary>
    private static string PrettyPrintJson(string raw)
    {
        var trimmed = raw.Trim();

        // Not JSON-like at all — return as-is
        if (!trimmed.StartsWith("{") && !trimmed.StartsWith("["))
            return trimmed;

        // ── Attempt 1: strict parse ──────────────────────────────────────────
        var pretty = TryParseJson(trimmed);
        if (pretty != null) return pretty;

        // ── Attempt 2: light cleanup then parse ──────────────────────────────
        var cleaned = trimmed
            // Single quotes → double quotes (but not inside already-double-quoted strings)
            .Replace("'", "\"")
            // Trailing commas before } or ]
            .Replace(",\n}", "\n}").Replace(",\n]", "\n]")
            .Replace(", }", " }").Replace(", ]", " ]");

        pretty = TryParseJson(cleaned);
        if (pretty != null) return pretty;

        // ── Attempt 3: manual indentation ────────────────────────────────────
        // Even if it's not valid JSON, indent it visually so it's readable
        return ManualIndent(trimmed);
    }

    private static string? TryParseJson(string text)
    {
        try
        {
            var options = new System.Text.Json.JsonDocumentOptions
            {
                AllowTrailingCommas = true,
                CommentHandling = System.Text.Json.JsonCommentHandling.Skip
            };
            using var doc = System.Text.Json.JsonDocument.Parse(text, options);
            return System.Text.Json.JsonSerializer.Serialize(
                doc.RootElement,
                new System.Text.Json.JsonSerializerOptions { WriteIndented = true });
        }
        catch
        {
            return null;
        }
    }

    /// <summary>
    /// Simple character-by-character indenter for JSON-like text
    /// when proper parsing fails.
    /// </summary>
    private static string ManualIndent(string text)
    {
        var sb     = new StringBuilder();
        int indent = 0;
        bool inStr = false;

        foreach (char ch in text)
        {
            if (ch == '"' && (sb.Length == 0 || sb[^1] != '\\'))
                inStr = !inStr;

            if (inStr)
            {
                sb.Append(ch);
                continue;
            }

            switch (ch)
            {
                case '{' or '[':
                    sb.Append(ch);
                    sb.AppendLine();
                    sb.Append(new string(' ', ++indent * 2));
                    break;
                case '}' or ']':
                    sb.AppendLine();
                    sb.Append(new string(' ', --indent * 2));
                    sb.Append(ch);
                    break;
                case ',':
                    sb.Append(ch);
                    sb.AppendLine();
                    sb.Append(new string(' ', indent * 2));
                    break;
                case ':':
                    sb.Append(": ");
                    break;
                default:
                    if (!(ch == ' ' && sb.Length > 0 && sb[^1] == ' '))
                        sb.Append(ch);
                    break;
            }
        }

        return sb.ToString().Trim();
    }

